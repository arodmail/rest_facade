package org.rest.facade.impl;

import org.rest.facade.RestArg;

public class ResourceArg implements RestArg {

    String id;
    String name;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}
